void lcd_transfer_command(uint16_t cmd)
{
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(DISPLAY_RS_GPIO_Port, DISPLAY_RS_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
	delay_display();
	HAL_GPIO_WritePin(DISPLAY_WR_GPIO_Port, DISPLAY_WR_Pin, GPIO_PIN_RESET);
	GPIOD->ODR &= 0x00FF;
	GPIOD->ODR |= cmd << 8;
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_SET);
	delay_display();
	
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
}

void lcd_transfer_data(uint16_t data)
{
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(DISPLAY_RS_GPIO_Port, DISPLAY_RS_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
	delay_display();
	HAL_GPIO_WritePin(DISPLAY_WR_GPIO_Port, DISPLAY_WR_Pin, GPIO_PIN_RESET);
	GPIOD->ODR &= 0x00FF;
	GPIOD->ODR |= data << 8;
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_SET);
	delay_display();
	
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
}

void lcd_init(void)
{
	HAL_GPIO_WritePin(DISPLAY_PWR_GPIO_Port, DISPLAY_PWR_Pin, GPIO_PIN_SET);
	HAL_Delay(50);
	
	HAL_GPIO_WritePin(DISPLAY_RES_GPIO_Port, DISPLAY_RES_Pin, GPIO_PIN_RESET);
	HAL_Delay(2);
	HAL_GPIO_WritePin(DISPLAY_RES_GPIO_Port, DISPLAY_RES_Pin, GPIO_PIN_SET);
	HAL_Delay(30);
	
	lcd_transfer_command(0x30);
	lcd_transfer_command(0x94);				//退出睡眠模式

	lcd_transfer_command(0x31);
	lcd_transfer_command(0xD7);				//自动读取控制
	lcd_transfer_data(0x9F);					//0x8F：使能自动读取	0x9F：不是能自动读取
	
	lcd_transfer_command(0x32);				//偏压比设置
	lcd_transfer_data(0x00);					//OSC Frequency adjustment
	lcd_transfer_data(0x01);					//Frequency on booster capacitors->6KHz
	lcd_transfer_data(0x00);					//Bias=1/14
	
	lcd_transfer_command(0x20); 			// Gray Level
	lcd_transfer_data(0x01);
	lcd_transfer_data(0x03);
	lcd_transfer_data(0x05);
	lcd_transfer_data(0x07);
	lcd_transfer_data(0x09);
	lcd_transfer_data(0x0B);
	lcd_transfer_data(0x0D);
	lcd_transfer_data(0x10);
	lcd_transfer_data(0x11);
	lcd_transfer_data(0x13);
	lcd_transfer_data(0x15);
	lcd_transfer_data(0x17);
	lcd_transfer_data(0x19);
	lcd_transfer_data(0x1B);
	lcd_transfer_data(0x1D);
	lcd_transfer_data(0x1F);
	
	lcd_transfer_command(0x31);
	lcd_transfer_command(0xF0);
	lcd_transfer_data(0x0F);
	lcd_transfer_data(0x0F);
	lcd_transfer_data(0x0F);
	
	lcd_transfer_command(0x30); 		//EXT=0
	lcd_transfer_command(0x75); 		//Page Address setting
	lcd_transfer_data(0x00); 				// XS=0
	lcd_transfer_data(0x13); 				// XE=19 0x13
	lcd_transfer_command(0x15); 		//Clumn Address setting
	lcd_transfer_data(0x00); 				// XS=0
	lcd_transfer_data(0xFF); 				// XE=256
	
	lcd_transfer_command(0xBC); 		//Data scan direction
	lcd_transfer_data(0x00); 				//MX.MY=Normal
	lcd_transfer_data(0xA6);
	
	lcd_transfer_command(0xCA); 		//Display Control
	lcd_transfer_data(0x00); 	
	lcd_transfer_data(0x9F); 				//Duty=160
	lcd_transfer_data(0x20); 				//Nline=off
	
	lcd_transfer_command(0xF0); 		//Display Mode
	lcd_transfer_data(0x10); 				//10=Monochrome Mode,11=4Gray
	
	lcd_transfer_command(0x81); 		//EV control
	lcd_transfer_data(0x35); 				//VPR[5-0] //0x35
	lcd_transfer_data(0x04); 				//VPR[8-6]
	
	lcd_transfer_command(0x20); 		//Power control
	lcd_transfer_data(0x0B); 				//D0=regulator ; D1=follower ; D3=booste, on:1 off:0
	HAL_Delay(10);
	clear_screen(1, 1, 20, 256);		
	lcd_transfer_command(0xAF); 		//Display on
}

/*功能：lcd寻址
*	page_start：起始页号码 1-20
*	column_start：起始列号码	1-256
*	page_total：一共寻址的页数	1-3 == 3
*	column_total：一共寻址的列数 1-3 == 3
*/
void lcd_address(uint8_t page_start, uint16_t column_start, uint8_t page_total, uint16_t column_total)
{
	uint8_t page_end;
	uint16_t colum_end;
	
	page_start = page_start - 1;
	column_start = column_start - 1;
	
	page_end = page_start + page_total - 1;
	colum_end = column_start + column_total - 1;
	
	lcd_transfer_command(0x75);			//Set Page Address	
	lcd_transfer_data(page_start);		
	lcd_transfer_data(page_end);	

	lcd_transfer_command(0x15);			//Set Column Address
	lcd_transfer_data(column_start);
	lcd_transfer_data(colum_end);
	
	lcd_transfer_command(0x30);			
	lcd_transfer_command(0x5c);			//写数据到屏幕
}

/*清屏*/
void clear_screen(uint8_t page_start, uint16_t column_start, uint8_t page_total, uint16_t column_total)
{
	uint8_t i;
	uint16_t j;
	
	lcd_address(page_start, column_start, page_total, column_total);
	
	for(i = 0;i<page_total; i++)
	{
		for(j=0; j<column_total; j++)
		{
			lcd_transfer_data(0x00);
		}
	}
}

