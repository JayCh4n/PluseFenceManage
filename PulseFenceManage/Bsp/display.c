void port_to_string(uint8_t *string, uint16_t port_num)
{
	uint8_t i;
	
	string[0] = port_num / 10000 + 0x30;
	string[1] = port_num / 1000 % 10 + 0x30;
	string[2] = port_num / 100 % 10 + 0x30;
	string[3] = port_num / 10 % 10 + 0x30;
	string[4] = port_num % 10 + 0x30;
	string[5] = '\0';
	
	for(i=0; i<5; i++)
	{
		if(string[i] != 0x30)
		{
			return;
		}
		string[i] = ' ';
	}
	
	string[4] = '0';
}

void clear_digit(uint8_t *p_numeral)
{
	uint8_t digit;
	
	digit = (*p_numeral)%10;
	*p_numeral -= digit;
}

void clear_digit_word(uint16_t *p_numeral)
{
	uint8_t digit;
	
	digit = (*p_numeral)%10;
	*p_numeral -= digit;
}

