void lcd_transfer_command(uint16_t cmd)
{
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(DISPLAY_RS_GPIO_Port, DISPLAY_RS_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
	delay_display();
	HAL_GPIO_WritePin(DISPLAY_WR_GPIO_Port, DISPLAY_WR_Pin, GPIO_PIN_RESET);
	GPIOD->ODR &= 0x00FF;
	GPIOD->ODR |= cmd << 8;
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_SET);
	delay_display();
	
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
}

void lcd_transfer_data(uint16_t data)
{
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_RESET);
	HAL_GPIO_WritePin(DISPLAY_RS_GPIO_Port, DISPLAY_RS_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
	delay_display();
	HAL_GPIO_WritePin(DISPLAY_WR_GPIO_Port, DISPLAY_WR_Pin, GPIO_PIN_RESET);
	GPIOD->ODR &= 0x00FF;
	GPIOD->ODR |= data << 8;
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_SET);
	delay_display();
	
	HAL_GPIO_WritePin(DISPLAY_CS_GPIO_Port, DISPLAY_CS_Pin, GPIO_PIN_SET);
	HAL_GPIO_WritePin(DISPLAY_E_GPIO_Port, DISPLAY_E_Pin, GPIO_PIN_RESET);
}

