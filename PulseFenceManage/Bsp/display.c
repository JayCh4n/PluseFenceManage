/*功能：lcd寻址
*	page_start：起始页号码 1-20
*	column_start：起始列号码	1-256
*	page_total：一共寻址的页数	1-3 == 3
*	column_total：一共寻址的列数 1-3 == 3
*/
void lcd_address(uint8_t page_start, uint16_t column_start, uint8_t page_total, uint16_t column_total)
{
	uint8_t page_end;
	uint16_t colum_end;
	
	page_start = page_start - 1;
	column_start = column_start - 1;
	
	page_end = page_start + page_total - 1;
	colum_end = column_start + column_total - 1;
	
	lcd_transfer_command(0x75);			//Set Page Address	
	lcd_transfer_data(page_start);		
	lcd_transfer_data(page_end);	

	lcd_transfer_command(0x15);			//Set Column Address
	lcd_transfer_data(column_start);
	lcd_transfer_data(colum_end);
	
	lcd_transfer_command(0x30);			
	lcd_transfer_command(0x5c);			//写数据到屏幕
}

/*清屏*/
void clear_screen(uint8_t page_start, uint16_t column_start, uint8_t page_total, uint16_t column_total)
{
	uint8_t i;
	uint16_t j;
	
	lcd_address(page_start, column_start, page_total, column_total);
	
	for(i = 0;i<page_total; i++)
	{
		for(j=0; j<column_total; j++)
		{
			lcd_transfer_data(0x00);
		}
	}
}

